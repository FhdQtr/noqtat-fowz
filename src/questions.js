// ─── مولد رياضيات عشوائي (أسئلة لا نهائية بدون تكرار) ──────────────────
export function generateRandomMathQuestion(difficulty = "easy") {
  const rand = (min, max) => Math.floor(Math.random() * (max - min + 1)) + min;

  if (difficulty === "easy") {
    const ops = [
      () => { const a = rand(10, 50), b = rand(10, 50); return { q: `${a} + ${b} = ؟`, answer: String(a + b) }; },
      () => { const a = rand(30, 99), b = rand(10, a); return { q: `${a} - ${b} = ؟`, answer: String(a - b) }; },
      () => { const a = rand(2, 12), b = rand(2, 12); return { q: `${a} × ${b} = ؟`, answer: String(a * b) }; },
    ];
    return ops[rand(0, ops.length - 1)]();
  }

  if (difficulty === "medium") {
    const ops = [
      () => { const a = rand(50, 200), b = rand(50, 200); return { q: `${a} + ${b} = ؟`, answer: String(a + b) }; },
      () => { const a = rand(100, 500), b = rand(50, a); return { q: `${a} - ${b} = ؟`, answer: String(a - b) }; },
      () => { const a = rand(12, 25), b = rand(3, 12); return { q: `${a} × ${b} = ؟`, answer: String(a * b) }; },
      () => { const b = rand(4, 16), r = rand(5, 30); const a = b * r; return { q: `${a} ÷ ${b} = ؟`, answer: String(r) }; },
    ];
    return ops[rand(0, ops.length - 1)]();
  }

  // hard
  const ops = [
    () => { const a = rand(200, 999), b = rand(100, 500); return { q: `${a} + ${b} = ؟`, answer: String(a + b) }; },
    () => { const a = rand(15, 30), b = rand(10, 20); return { q: `${a} × ${b} = ؟`, answer: String(a * b) }; },
    () => { const bases = [4, 9, 16, 25, 36, 49, 64, 81, 100, 121, 144, 169, 196, 225]; const n = bases[rand(0, bases.length-1)]; return { q: `√${n} = ؟`, answer: String(Math.sqrt(n)) }; },
    () => { const a = rand(2, 12); return { q: `${a}² = ؟`, answer: String(a * a) }; },
    () => { const a = rand(2, 5); return { q: `${a}³ = ؟`, answer: String(a * a * a) }; },
    () => { const a = rand(50, 200), b = rand(30, 150), c = rand(20, 100); return { q: `${a} + ${b} + ${c} = ؟`, answer: String(a + b + c) }; },
  ];
  return ops[rand(0, ops.length - 1)]();
}

// دالة توليد مجموعة أسئلة رياضيات عشوائية
export function generateMathBatch(count = 5, difficulty = "mixed") {
  const results = [];
  const diffs = difficulty === "mixed" ? ["easy", "medium", "hard"] : [difficulty];
  for (let i = 0; i < count; i++) {
    const d = diffs[i % diffs.length];
    results.push({ ...generateRandomMathQuestion(d), type: "input", mode: "speed" });
  }
  return results;
}


